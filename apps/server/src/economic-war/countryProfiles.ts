/** Répartit un capital agricole global en ressources alimentaires granulaires */
function food(agri: number) {
  return {
    cereals:    Math.round(agri * 0.40),
    vegetables: Math.round(agri * 0.25),
    sugarOils:  Math.round(agri * 0.20),
    fodder:     Math.round(agri * 0.15),
    redMeat: 0, whiteMeat: 0, dairy: 0, fish: 0,
    // Biens manufacturés — tous à 0 au départ (produits en jeu)
    steel: 0, fuel: 0, electronicComponents: 0, pharmaceuticals: 0,
    processedFood: 0, fertilizer: 0, phones: 0, computers: 0,
    munitions: 0, obus: 0, bombs: 0,
  };
}

